///////////////////////////////////////////////////////////////////////////
// + another inlineString
///////////////////////////////////////////////////////////////////////////
inlineString& inlineString::operator + (inlineString &toAdd)
{
	static inlineString toRet(m_contents, m_length);
	strcat((char*)toRet, (char*)toAdd);
	return toRet;
}

